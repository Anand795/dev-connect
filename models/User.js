const mongoose = require("mongoose");

// Create a model class for user 
const UserSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  avatar: {
    type: String,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});

// takes in two params -> model name and schema
module.exports = User = mongoose.model("user", UserSchema)